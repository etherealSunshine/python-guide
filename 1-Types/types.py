print(type('Hello!')) # becomes <class 'str'> when printed
print(type("World!")) # also prints <class 'str'>

print(type(11))       # becomes <class 'int'>
print(type(11.0))     # becomes <class 'float'>
print(type(3 + 11j))  # becomes <class 'complex'>

print(type(True))     # becomes <class 'bool'>
print(type(False))    # becomes <class 'bool'>